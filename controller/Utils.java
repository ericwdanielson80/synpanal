import java.io.File;
import javax.swing.ImageIcon;

/* Utils.java is used by FileChooserDemo2.java. */
/**
 * Small static helper class with string constants for recognised image
 * file extensions (jpeg, jpg, gif, tiff, tif, png), a utility that
 * extracts a lowercase extension from a File's name, and a helper that
 * loads an ImageIcon from a classpath resource path. The class is used
 * by file chooser filters and by icon-loading code in the UI.
 */
public class Utils {
    public final static String jpeg = "jpeg";
    public final static String jpg = "jpg";
    public final static String gif = "gif";
    public final static String tiff = "tiff";
    public final static String tif = "tif";
    public final static String png = "png";

    /*
     * Get the extension of a file.
     */
    /**
     * Extracts the lowercase extension from a file's name. The local s
     * is the file name, i is the index of the last dot, and ext is the
     * substring after that dot (or null when no extension is present or
     * the dot is the final character). The parameter f is the file to
     * inspect.
     */
    public static String getExtension(File f) {
        String ext = null;
        String s = f.getName();
        int i = s.lastIndexOf('.');

        if (i > 0 &&  i < s.length() - 1) {
            ext = s.substring(i+1).toLowerCase();
        }
        return ext;
    }

    /**
     * Returns an ImageIcon loaded from the given classpath resource
     * path, or null if the path cannot be resolved. The local imgURL
     * is the resolved resource URL; on failure an error is printed to
     * stderr. The parameter path is the classpath-relative image path.
     */
    protected static ImageIcon createImageIcon(String path) {
        java.net.URL imgURL = Utils.class.getResource(path);
        if (imgURL != null) {
            return new ImageIcon(imgURL);
        } else {
            System.err.println("Couldn't find file: " + path);
            return null;
        }
    }
}
